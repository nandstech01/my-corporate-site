/**
 * プロンプト注入用フォーマッタ
 *
 * @param mode - 'short' = 短文投稿用、'article' = 長文記事用
 */
export function formatVoiceProfileForPrompt(mode: 'short' | 'article' | 'linkedin'): string {
  if (mode === 'linkedin') {
    return `## ブランドボイス（LinkedIn）
- プロフェッショナルだが堅すぎない語り口
- 署名表現: 「正直すごいなと」「控えめに言って」「ぶっちゃけ」は使ってOK
- 「〜ですw」「以上w」等のカジュアルすぎる表現はLinkedInでは避ける

## NG表現（AI感が出るため禁止）
${VOICE_PROFILE.aiSmellPatterns.map((p) => `- 「${p}」`).join('\n')}

## 構造的注意
- AI感のある均一な段落構成を避ける
- 個人的な体験・感情を必ず含める`
  }

  const examples =
    mode === 'article'
      ? VOICE_PROFILE.fewShotExamplesArticle
      : VOICE_PROFILE.fewShotExamplesShort

  const examplesFormatted = examples
    .map((ex, i) => `--- 例${i + 1} ---\n${ex}`)
    .join('\n\n')

  // ランダムサンプリング: 50個中8-12個を選択して提示
  const sampleCount = 8 + Math.floor(Math.random() * 5)
  const shuffled = [...VOICE_PROFILE.signatureExpressions]
    .sort(() => Math.random() - 0.5)
    .slice(0, sampleCount)

  // 締め方も6-8個をランダム選択
  const endingSampleCount = 6 + Math.floor(Math.random() * 3)
  const shuffledEndings = [...VOICE_PROFILE.endings]
    .sort(() => Math.random() - 0.5)
    .slice(0, endingSampleCount)

  return `## ボイスプロファイル（@nands_tech の口調）

### 署名表現（自然に0-3個/投稿で使え。使わない投稿もあっていい）
${shuffled.map((e) => `「${e}」`).join('、')}
※署名表現は文中で使え。文末には使うな。毎回全部使う必要はない。

### 締め方（文末はこの中から選べ）
${shuffledEndings.map((e) => `「${e}」`).join('、')}

### AI感を出すNG表現（絶対に使うな）
${VOICE_PROFILE.aiSmellPatterns.map((p) => `- 「${p}」`).join('\n')}

### 構造的注意
- 全段落を同じ長さ・構造にするな（短い段落と長い段落を混ぜろ）
- 括弧内の独り言やツッコミを挟め（「(いや、これマジで？w)」）
- 自虐的ユーモアや読者の内心を代弁する対話を入れろ
- 個人的な体験・感情表現を必ず含めろ

### 参考例（このトーンを真似ろ）
${examplesFormatted}`
}
